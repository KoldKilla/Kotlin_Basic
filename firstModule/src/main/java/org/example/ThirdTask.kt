package org.example

fun main(args: Array<String>){
    var five: Char = '5'

    println("String: ${five.toString()}")
    println("Int: ${five.toInt()}")
    println("Short: ${five.toShort()}")
    println("Byte: ${five.toByte()}")
    println("Double: ${five.toDouble()}")
    println("Float: ${five.toFloat()}")
    println("Long: ${five.toLong()}")
}